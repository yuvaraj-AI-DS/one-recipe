const grid = document.getElementById('recipeGrid');
const searchInput = document.getElementById('searchInput');
const filterBtns = document.querySelectorAll('.filter-btn');

function displayRecipes(filteredList) {
    grid.innerHTML = filteredList.map(recipe => `
        <div class="recipe-card" data-id="${recipe.id}">
            <img src="${recipe.img}" alt="${recipe.title}" class="recipe-img">
            <div class="recipe-content">
                <span class="tag">${recipe.category}</span>
                <h3>${recipe.title}</h3>
                <p>Ready in: ${recipe.time}</p>
            </div>
        </div>
    `).join('');
}

// Search Logic
searchInput.addEventListener('input', (e) => {
    const term = e.target.value.toLowerCase();
    const filtered = recipes.filter(r => r.title.toLowerCase().includes(term));
    displayRecipes(filtered);
});

// Filter Logic
filterBtns.forEach(btn => {
    btn.addEventListener('click', () => {
        filterBtns.forEach(b => b.classList.remove('active'));
        btn.classList.add('active');
        
        const category = btn.dataset.filter;
        const filtered = category === 'all' ? recipes : recipes.filter(r => r.category === category);
        displayRecipes(filtered);
    });
});



const modal = document.getElementById('recipeModal');
const modalDetails = document.getElementById('modalDetails');
const closeBtn = document.querySelector('.close-btn');

function openRecipe(id) {
    const recipe = recipes.find(r => r.id === id);
    
    modalDetails.innerHTML = `
        <h2>${recipe.title}</h2>
        <img src="${recipe.img}" style="width:100%; border-radius:10px;">
        <h3>Ingredients</h3>
        <ul>${recipe.ingredients.map(ing => `<li>${ing}</li>`).join('')}</ul>
        <h3>Instructions</h3>
        <ol>${recipe.instructions.map(step => `<li>${step}</li>`).join('')}</ol>
    `;
    modal.style.display = "block";
}

// Close modal when clicking 'X' or outside the box
closeBtn.onclick = () => modal.style.display = "none";
window.onclick = (event) => { if (event.target == modal) modal.style.display = "none"; };

// Initial Load
displayRecipes(recipes);

grid.addEventListener('click', (e) => {
    const card = e.target.closest('.recipe-card');
    if (!card) return;

    const id = Number(card.dataset.id);
    openRecipe(id);
});